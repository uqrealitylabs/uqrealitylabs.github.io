function GetSubpage() {
    let path = window.location.pathname.split('/');
    path = path.slice(2);
    path = path.join("/");

    if (path == "" || path == "students") {
        return <Students />
    } else if (path == "sponsors") {
        return <Industry />
    } else {
        window.location.pathname = "404.html"
    }
}

